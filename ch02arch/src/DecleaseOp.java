public class DecleaseOp {

	public static void main(String[] args) {

		int i = 20;

		--i;
		// i를 1감소 시킨다.

		System.out.println(i);
		// i를 출력한다. // 19

		System.out.println(--i);
		// i를 1감소 시킨후 출력하세요를 한줄 명령어 // 18

		System.out.println(i);
		// i를 출력하세요. // 18

		i--;
		// i를 1감소 시키세요.

		System.out.println(i);
		// i를 출력하세요. // 17

		System.out.println(i--);
		// i를 먼저 출력하고 후 1감소 시키세요. 한줄 명령어 // 17

	}

}
